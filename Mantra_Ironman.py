mantra = ["Confío en mí. He entrenado lo mejor que he podido y domino la distancia, por ello nadaré, pedalearé y correré para mí."]
mantra.append("Los demás compiten conmigo, pero no son mis adversarios. Compito para mí.")
mantra.append("Debo ver el triatlón como algo divertido. Alegría y diversión pese a la dificultad. Porque tengo el privilegio de deslizarme.")
print("\nMantra del Ironman")
n = 1
for item in mantra:
    print(f"{n}: {item}")
    n += 1
print()
